package com.kpi;

public class FourthTask {
    double[][] transpose(double[][] matrix) {
        double[][] transposed = new double[matrix.length][matrix[0].length];
        for (int i = 0; i < matrix.length; i++) {
            for (int j = 0; j < matrix[0].length; j++) {
                transposed[j][i] = matrix[i][j];
            }
        }
        return transposed;
    }
}
